/* ── Translate any Arabic string dynamically ── */
function translateDynamic(text, lang) {
  if(!text || lang === 'ar') return text;
  if(!AR_LOOKUP[text]) return text;
  return AR_LOOKUP[text][lang] || text;
}

/* ── Replace Arabic text in a DOM node recursively ── */
function translateNode(node, lang) {
  if(lang === 'ar') return;
  if(node.nodeType === 3) { // Text node
    const trimmed = node.textContent.trim();
    if(trimmed.length > 1 && /[\u0600-\u06FF]/.test(trimmed)) {
      const translated = translateDynamic(trimmed, lang);
      if(translated !== trimmed) node.textContent = node.textContent.replace(trimmed, translated);
    }
  } else if(node.nodeType === 1 && !['SCRIPT','STYLE','INPUT','TEXTAREA'].includes(node.tagName)) {
    node.childNodes.forEach(child => translateNode(child, lang));
  }
}

